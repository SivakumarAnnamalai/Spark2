object CreateVariablesWithDatatype extends App{

  // Numeric Variables
  var v1:Int = 10       // Int
  var v2:Long = 10L      // Long
  var v3:Long = 10l      // Long

  var v4:Float = 10.0F    // Float
  var v5:Float = 10.0f    // Float
  var v6:Double = 10.0     // Double

  // String/Char variables
  var v7:String = "Spark"
  var v8:Char = 'S'

  // Boolean
  var v9:Boolean = true

  // Byte and Short
  var v10:Byte = 50
  var v11:Short = 100

}
